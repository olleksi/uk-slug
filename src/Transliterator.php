<?php

namespace Olleksi\UkSlug;

class Transliterator
{
    /**
     * Транслітерація українського тексту в латиницю для URL
     * 
     * @param string $string
     * @return string
     */
    public static function transliterate($string)
    {
        $translitMap = [
            // Великі літери
            'А' => 'A', 'Б' => 'B', 'В' => 'V', 'Г' => 'H', 'Ґ' => 'G',
            'Д' => 'D', 'Е' => 'E', 'Є' => 'Ye', 'Ж' => 'Zh', 'З' => 'Z',
            'И' => 'Y', 'І' => 'I', 'Ї' => 'Yi', 'Й' => 'Y', 'К' => 'K',
            'Л' => 'L', 'М' => 'M', 'Н' => 'N', 'О' => 'O', 'П' => 'P',
            'Р' => 'R', 'С' => 'S', 'Т' => 'T', 'У' => 'U', 'Ф' => 'F',
            'Х' => 'Kh', 'Ц' => 'Ts', 'Ч' => 'Ch', 'Ш' => 'Sh', 'Щ' => 'Shch',
            'Ь' => '', 'Ю' => 'Yu', 'Я' => 'Ya',
            
            // Малі літери
            'а' => 'a', 'б' => 'b', 'в' => 'v', 'г' => 'h', 'ґ' => 'g',
            'д' => 'd', 'е' => 'e', 'є' => 'ie', 'ж' => 'zh', 'з' => 'z',
            'и' => 'y', 'і' => 'i', 'ї' => 'i', 'й' => 'i', 'к' => 'k',
            'л' => 'l', 'м' => 'm', 'н' => 'n', 'о' => 'o', 'п' => 'p',
            'р' => 'r', 'с' => 's', 'т' => 't', 'у' => 'u', 'ф' => 'f',
            'х' => 'kh', 'ц' => 'ts', 'ч' => 'ch', 'ш' => 'sh', 'щ' => 'shch',
            'ь' => '', 'ю' => 'iu', 'я' => 'ia',
        ];

        // Замінюємо українські літери на латиницю
        $slug = strtr($string, $translitMap);
        
        // Видаляємо всі символи, окрім латиниці, цифр, пробілів та дефісів
        $slug = preg_replace('/[^A-Za-z0-9\s-]/', '', $slug);
        
        // Замінюємо пробіли та дефіси на один дефіс
        $slug = preg_replace('/[\s-]+/', '-', $slug);
        
        // Видаляємо дефіси на початку та в кінці, переводимо в нижній регістр
        $slug = strtolower(trim($slug, '-'));
        
        // Якщо слаг порожній, генеруємо щось за замовчуванням
        if (empty($slug)) {
            $slug = 'discussion';
        }
        
        return $slug;
    }
}
